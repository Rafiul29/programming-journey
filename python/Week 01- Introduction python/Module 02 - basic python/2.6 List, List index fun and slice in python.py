# List, Array, Collection is same (simple terms)


#index   0  1  2  3  4  5  6  7 
numbers=[5,10,15,20,25,30,40,50]
# numbers[0]=31
print(numbers)
#index  -8 -7 -6 -5 -4 -3 -2 -1
print(numbers[-1])
#list(start:end)  # start from the start index and stops before the end index
print(numbers[2:6])
print(numbers[-6:-1])

#list(start:end:step)
print(numbers[1:7:2])
print(numbers[7:2:-1])
print(numbers[2:])
print(numbers[:4])
print(numbers[:])
print(numbers[::-1])

